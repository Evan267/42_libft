int	memcmp(const void *s1, const void *s2, size_t n)
{
	char	*str1;
	char	*str2;

	str1 = s1;
	str2 = s2;
	while (*str1 == *str2)
	{
		str1++;
		str2++;
	}
	return (*str1 - *str2);
}
